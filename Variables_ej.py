# Listas
lista = [1, 2, 3, 4, 5]

print(lista)

# Tuplas
tupla = ("1, 2, 3")

print(tupla)

# N° de textos
texto = "Leonardo"

print(texto)

# N° de enteros
números = 10 + 10

print(números)

# Float
Pí = 3.14

print(Pí)

# Entero o int
A = 40

print(A)

# Conjuntos
fs = frozenset([1, 2, 3])

print(type(fs))

# String
A = "Esto es un string"

print(A)

# Diccionario
persona = {"nombre": "Leonardo", 
           "edad": 40}

print(persona)

# Booleano
condición = True

print(condición)